    public ketQua(tongHopLK dsKQ) {
        initComponents();
        this.setTitle("Kết quả lựa chọn");
        kqChon = dsKQ;
        String[] tenLoai = {"CPU","GPU","Mainboard","RAM","HDD","SSD","Case"};
        int tong=0;
        DefaultTableModel model = (DefaultTableModel) (tblKQ.getModel());
        for(int i=0;i<tblKQ.getRowCount();i++){
            for(int j=0;j<kqChon.getdsdv().length;j++){
                if(kqChon.getLoai(j).equals(tenLoai[i]) == true){
                    model.setValueAt(kqChon.getDV(j).getID(), i, 1);
                    model.setValueAt(getTenLK(kqChon.getDV(j).getID()), i, 2);
                    model.setValueAt(kqChon.getDV(j).getGia(), i, 3);
                    tong = tong + kqChon.getDV(j).getGia();
                }
            }
        }
        tblKQ.setModel(model);
        txtTGT.setText(String.valueOf(kqChon.getTGT()));
        txtThua.setText(String.valueOf(kqChon.getW()));
        txtTong.setText(String.valueOf(tong));
    }

