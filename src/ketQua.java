    private String getTenLK(int id){
        PreparedStatement ps;
        ResultSet rs;
        Connection con = DBConnect.getConnection();
        String tenLK = new String();
              
        //Cap nhat Hang
        try{   
            ps = con.prepareStatement("SELECT `Ten_LK` FROM `linhkien` WHERE `ID_LK`=?" );       
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while(rs.next()){
                tenLK = rs.getString(1);
            }
        }
        catch(Exception e){
            System.out.println("Có gì sai sai!");
        }
        
        try{
            con.close();
        }
        catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        return tenLK;
    }
